"use client";

import { GoogleOAuthProvider } from "@react-oauth/google";
import { ReactNode } from "react";

export default function GoogleOAuthProviderWrapper({
  children,
}: {
  children: ReactNode;
}) {
  const clientId = process.env.NEXT_PUBLIC_GOOGLE_CLIENT_ID || "";

  if (!clientId) {
    console.warn("Google Client ID is not set in environment variables");
    return <>{children}</>;
  }

  return (
    <GoogleOAuthProvider
      clientId={clientId}
      onScriptLoadError={() =>
        console.warn("Failed to load Google OAuth script")
      }
      onScriptLoadSuccess={() =>
        console.log("Google OAuth script loaded successfully")
      }
    >
      {children}
    </GoogleOAuthProvider>
  );
}